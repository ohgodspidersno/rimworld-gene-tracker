from gene.constants import GeneCategory


def is_cosmetic_color(gene):
    return gene.category == GeneCategory.COSMETIC and any(
        [s in gene.def_name for s in ['Hair_', 'Skin_', 'Eye_']]
    ) and all(
        s not in gene.def_name for s in ['BaldOnly', 'ShortOnly', 'LongOnly', 'Grayless',]
    )


def generate_color(gene):
    if is_cosmetic_color(gene):
        return gene.def_name.replace('Hair_', '').replace('Skin_', '').replace('Eye_', '')
    else:
        return None

